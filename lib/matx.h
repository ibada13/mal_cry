void pr_tab_hex(char* tab , size_t size ){
    uint8_t *pointer = (uint8_t *) tab  ;
    for (int i = 0; i < size; i++){
        if(pointer[i] == 0){
            printf("0x00, ");
        }
        else{
        printf("%#04x, ", pointer[i]);
        }

    }
    printf("\n");
}
